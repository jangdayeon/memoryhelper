package javabean;

import java.util.Date;

public class postBean {
	private String id;
	private String category;
	private Date postW;
	private String postT;
	private String postC;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public Date getPostW() {
		return postW;
	}
	public void setPostW(Date postW) {
		this.postW = postW;
	}
	public String getPostT() {
		return postT;
	}
	public void setPostT(String postT) {
		this.postT = postT;
	}
	public String getPostC() {
		return postC;
	}
	public void setPostC(String postC) {
		this.postC = postC;
	}
	
	
}
